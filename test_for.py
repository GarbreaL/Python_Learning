print('range(10)')
for i in range(10):
    print(i, end='\n')

print('range(0,10)')
for i in range(0, 10):
    print(i, end='\n')

print('range(0,10,1)')
for i in range(0, 10,1):
    print(i, end='\n')

for i in range(10):
    j = i+1
    print(j, end='\n')
    
while (i < 10):
    j = i + 1
    print(j, end='\n')
    i=i+1